DROP TABLE IF EXISTS address_book;
CREATE TABLE address_book
(
    id            BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY, -- 自增主键
    user_id       BIGINT      NOT NULL,
    consignee     VARCHAR(50),
    sex           VARCHAR(2),
    phone         VARCHAR(11) NOT NULL,
    province_code VARCHAR(12),
    province_name VARCHAR(32),
    city_code     VARCHAR(12),
    city_name     VARCHAR(32),
    district_code VARCHAR(12),
    district_name VARCHAR(32),
    detail        VARCHAR(200),
    label         VARCHAR(100),
    is_default    BOOLEAN     NOT NULL DEFAULT FALSE
);

DROP TABLE IF EXISTS category;
CREATE TABLE category
(
    id     BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- 自增主键
    name   VARCHAR(32) NOT NULL,                                -- 分类名称
    sort   INT         NOT NULL DEFAULT 0,                      -- 顺序
    status INT,                                                 -- 分类状态 0:禁用，1:启用
    CONSTRAINT idx_category_name UNIQUE (name)                  -- 唯一索引
);

